# A Estoca permite ate 50 SKUs por chamada (confirmado na doc oficial v1).
LOTE_SIZE = 50
MAX_RETRIES = 2
TEMPO_ESPERA = 5


def rodarAPIEstoca():
    """
    Consulta o inventario na Estoca em lotes.
    Retorna lista de dicts: [{"sku": ..., "available": ...}, ...]
    """
    base_url = os.getenv("BASE_URL_ESTOCA")        # ex: https://api.estoca.com.br
    api_key = os.getenv("API_KEY_ESTOCA")
    warehouse = os.getenv("WAREHOUSE")
    endpoint = os.getenv("ESTOCA_ENDPOINT", "/inventories")

    if not all([base_url, api_key, warehouse]):
        raise RuntimeError(
            "Variaveis de ambiente da Estoca ausentes. "
            "Confira BASE_URL_ESTOCA, API_KEY_ESTOCA e WAREHOUSE."
        )

    url = base_url.rstrip("/") + endpoint
    headers = {
        "X-Api-Key": api_key,
        "X-Api-Version": "v1",
    }

    resultados = []

    for i in range(0, len(SKUS), LOTE_SIZE):
        lote = SKUS[i:i + LOTE_SIZE]
        params = {
            "warehouse": warehouse,
            "skus": ",".join(lote),
        }
        num_lote = i // LOTE_SIZE + 1

        for tentativa in range(1, MAX_RETRIES + 1):
            try:
                response = requests.get(url, headers=headers, params=params, timeout=30)
                response.raise_for_status()

                dados = response.json()
                data = dados.get("data", [])

                # A API retorna dict para 1 SKU e lista para varios. Normaliza.
                if isinstance(data, dict):
                    data = [data]

                for produto in data:
                    resultados.append({
                        "sku": produto["product_sku"],
                        "available": produto["available"],
                    })

                log.info(f"Lote {num_lote} OK, produtos processados: {len(data)}")
                break

            except requests.exceptions.RequestException as e:
                log.warning(f"Erro no lote {num_lote}, tentativa {tentativa}: {e}")
                if tentativa < MAX_RETRIES:
                    time.sleep(TEMPO_ESPERA)
                else:
                    log.error(f"Lote {num_lote} falhou definitivamente")

    log.info(f"Total de produtos recebidos: {len(resultados)}")
    return resultados
